# Usando o método keys() para obter todas as chaves de um dicionário

contatos = {"guilherme@gmail.com": {"nome": "Guilherme", "telefone": "3333-2221"}}

# código professor
resultado = contatos.keys()  # dict_keys(['guilherme@gmail.com'])
print(resultado)
# meu código
print(contatos.keys())  # dict_keys(['guilherme@gmail.com'])    