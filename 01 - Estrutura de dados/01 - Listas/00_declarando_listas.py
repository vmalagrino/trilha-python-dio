# todas as listas abaixo são válidas em Python

frutas = ["laranja", "maca", "uva"]
print(frutas)

# vazia, não necessariamente possui valores
frutas = []
print(frutas)

# construtor list, faz com que cada letra seja um elemento da lista. Vem de um elemento iterável
letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
