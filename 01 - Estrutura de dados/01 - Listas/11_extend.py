# método extend adiciona múltiplos elementos ao final da lista

# extend junta listas
# append adiciona um único elemento (que pode ser uma lista)

linguagens = ["python", "js", "c"]

print(linguagens)  # ["python", "js", "c"]

linguagens.extend(["java", "csharp"])
print(linguagens)  # ["python", "js", "c", "java", "csharp"]
